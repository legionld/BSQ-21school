#include <unistd.h>
#include "head.h"

void	print_map(void)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (g_map[i])
	{
		j = 0;
		while (g_map[i][j] != '\0')
		{
			write(1, &g_map[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
